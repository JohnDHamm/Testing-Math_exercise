function add(first, second) {
	return first + second;
}

function subtract(first, second) {
	return first - second;
}

function multiply(first, second){
	return first * second;
}

function divide(first, second) {
	return first / second;
}

function square (num) {
	return num * num;
}

function squareRoot(num) {
	return Math.sqrt(num);
}








